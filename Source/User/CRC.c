/********************************************************************

CRC16校验，用于单元板数据校验

*********************************************************************/
uint16_t CRC16(uint8_t* da, uint8_t len) {
	uint8_t i, n;
	uint8_t CRCH, CRCL;

	CRCH = 0xff;
	CRCL = 0xff;
	for (i = 0; i < len; i++) {
		n = CRCH ^ (*da);
		CRCH = CRCL ^ crc_hi[n];
		CRCL = crc_lo[n];
		da++;
	}
	return ((CRCH << 8) + CRCL);
}

/******************************************************************

CRC8校验

*******************************************************************/
uint8_t CRC8(uint8_t* da, uint8_t cnt) {
	uint8_t re0;
	uint8_t result;

	cnt &= 0x0f;

	result = 0x0;
	for (re0 = 0; re0 < cnt; re0++) {
		result = crc8tab[result ^ (*da)];
		da++;
	}
	return (~result);
}
